export function getImageCategory(category){
  switch (category) {
    case "Pastas":
      return "./img/categories/pastas.png";
    case "Meat":
      return "./img/categories/meat.png";
    case "Salads":
      return "./img/categories/salads.png";
    case "Desserts":
      return "./img/categories/desserts.png";
    }
}
